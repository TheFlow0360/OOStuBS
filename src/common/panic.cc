void Panic::trigger(){
  kout<<"Unhandled Interrupt: "<<interruptNr<<"!"<<endl;
  cpu.halt();
}

void Panic::currentInterrupt(int iNum){
  interruptNr = iNum;
}

int Panic::getCurrentInterrupt()
{
  return interruptNr;
}
